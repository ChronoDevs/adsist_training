    public function postLogin(Request $request)
    {   
        $request->validate([
            'email' => ['required','email'],
            'password'=> ['required', ]
        ]);

        $credentials = $request->only('email', 'password');
        if (Auth::attempt($credentials)) {
            return redirect() -> intended('/')->withSuccess('You have Logged In!');
        }
        
         return redirect("login")->withSuccess('You have entered an Invalid credentials');
    }

